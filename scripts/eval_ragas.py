"""Evaluate ARAG system using RAGAS metrics."""

import json
from datasets import Dataset
from ragas import evaluate
from ragas.metrics import (
    faithfulness,
    answer_relevancy,
    context_precision,
    context_recall,
)
from langchain_openai import ChatOpenAI, OpenAIEmbeddings

def run_ragas_evaluation(predictions_file: str):
    print(f"📊 Loading predictions from {predictions_file}...")
    with open(predictions_file, 'r', encoding='utf-8') as f:
        results = json.load(f)

    # Construct RAGAS Dataset format
    data_samples = {
        "question": [],
        "answer": [],        # Final answer generated by Agent
        "contexts": [],      # Original text read by Agent during read_chunk
        "ground_truth": []   # Ground truth answer
    }

    for res in results:
        data_samples["question"].append(res["question"])
        data_samples["answer"].append(res["predicted_answer"])
        data_samples["ground_truth"].append(res["ground_truth"])
        
        # Core logic: extract text actually read by Agent from trajectory (black box) as contexts
        read_contexts = []
        for step in res.get("trajectory", []):
            if step["tool_name"] == "read_chunk" and "tool_result" in step:
                read_contexts.append(step["tool_result"])
        
        # If Agent failed to read any chunks, use empty string as placeholder
        if not read_contexts:
            read_contexts = [""]
        data_samples["contexts"].append(read_contexts)

    dataset = Dataset.from_dict(data_samples)

    # Must specify LLM judge (typically use GPT-4o to ensure evaluation quality)
    evaluator_llm = ChatOpenAI(model="gpt-4o")
    evaluator_embeddings = OpenAIEmbeddings(model="text-embedding-3-small")

    print("⚖️ Running RAGAS Evaluation... (This may take a while as the LLM judges each aspect)")
    
    # Execute multi-dimensional evaluation
    evaluation_result = evaluate(
        dataset,
        metrics=[
            context_precision, # Retrieval precision (tests Reranker)
            context_recall,    # Coverage (tests BM25 & Milvus)
            faithfulness,      # Hallucination check (tests Agent's System Prompt)
            answer_relevancy,  # Answer relevance
        ],
        llm=evaluator_llm,
        embeddings=evaluator_embeddings
    )

    print("\n" + "="*50)
    print("🏆 RAGAS Evaluation Results:")
    print("="*50)
    for metric_name, score in evaluation_result.items():
        print(f"{metric_name.ljust(20)}: {score:.4f}")
    print("="*50)
    
    # Export detailed per-row report for debugging
    df = evaluation_result.to_pandas()
    df.to_csv("./data/ragas_evaluation_report2.csv", index=False)
    print("📁 Detailed report saved to ./data/ragas_evaluation_report2.csv")

if __name__ == "__main__":
    run_ragas_evaluation("./data/predictions.json")